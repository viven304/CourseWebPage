function hideOverlay() {
    $(function() {
        $('#sqr').slideToggle();
        $("#textOnOverlay").slideToggle();
    })
}

function reduceOpacity1() {
    $(function() {
        $(".1").animate({
            opacity: '-=0.5'
          });
    });
}


